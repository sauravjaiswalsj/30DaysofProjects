 const _={};

 _.filter=(list, callback)=>{
     //create a new array 
     // Loop through the array 
     // Check back  if callback returns true the push data to array/
     // return the array
    const storage=[];
    list.forEach(element => {
        if(callback(element)===true){
            storage.push(element);
        }
    });
    return storage; 
 }

